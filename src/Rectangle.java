public class Rectangle {
    int length;
    int width;
    int xPos;
    int yPos;


    public Rectangle(int length,int width, int xPos , int yPos){
        this.length = length;
        this.width = width;
        this.xPos = xPos;
        this.yPos = yPos;
    }
    public int getLength(){
        return length;
    }
    public int getWidth(){
        return width;
    }
    public int getxPos(){
        return xPos;
    }
    public int getyPos(){
        return yPos;
    }






}
